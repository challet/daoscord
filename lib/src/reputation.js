import logger from "./logger.js";
import {getDb} from "./db-manager.js";
import {Contract} from "@ethersproject/contracts";
import {Wallet} from "@ethersproject/wallet";
import {JsonRpcProvider} from "@ethersproject/providers";

export const allotReputationToMembers = async (allocations, adminPrivateKey, rpcUrl) => {
    logger.debug(`Allot reputation tokens to ${allocations.length} members...`)
    const db = getDb()
    const tokenAddress = db.data.erc20TokenAddress
    if(!tokenAddress){
        throw new Error('DAO has not been deployed yet.')
    }
    const provider = new JsonRpcProvider(rpcUrl)
    const wallet = new Wallet(adminPrivateKey, provider)
    const contract = new Contract(tokenAddress, erc20TokenAbi, wallet);
    for (const allocation of allocations) {
        const memberUuid = allocation.memberUuid
        const memberAddress = db.data.members[memberUuid].address
        const reputation = allocation.reputation
        const tx = await contract.allot(memberAddress, reputation)
        await tx.wait()
    }
    logger.debug(`Allot reputation tokens to ${allocations.length} members done.`)
}

